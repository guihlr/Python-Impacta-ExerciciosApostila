'''
Objetivo: Calcular o salário sabendo o valor da hora e o número
de horas trabalhadas
Entrada: Valor da hora e número de horas trabalhadas
Saída: O salário
'''
hora=float(input("Digite o valor da hora: "))
num_Horas_Trab=int(input("Digite o número de horas trabalhadas: "))
salario=hora*num_Horas_Trab
print(salario)


